    void omplirLlista(String tipus) {
        DefaultComboBoxModel object = new DefaultComboBoxModel();
        ArrayList<String> llista = (tipus == "clients") ? (adaptador.llistarClients()) : (adaptador.llistarArticles());
        for (String cosa : llista) {
            object.addElement(cosa);
        }
        if (tipus == "clients") {
            comboClients.setModel(object);
        } else {
            comboArticles.setModel(object);
        }
    }

