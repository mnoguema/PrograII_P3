    /**
     * Añade un nuevo artículo a la lista de artículos del mercado. Recoge los
     * datos de los campos de texto y comprueba si se ha marcado la casilla de
     * si el nuevo artículo admite o no envíos urgentes. Si ya existe un
     * artículo con el id indicado, muestra un mensaje indicando el error y no
     * guarda el artículo.
     *
     * @param evt evento correspondiente a clicar sobre el botón aceptar.
     */
    private void btnAcceptaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptaActionPerformed
        if (txtQuantitat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Alguna casella és en blanc.", "Atenció", INFORMATION_MESSAGE);
            return;
        }
        try {
            adaptador.afegirComanda(comboArticles.getSelectedIndex(), comboClients.getSelectedIndex(), Integer.parseInt(txtQuantitat.getText()), chkUrgent.isSelected());
            this.dispose();
        } catch (NumberFormatException numberException) {
            JOptionPane.showMessageDialog(this, "El preu o temps d'enviament proporcionat no és vàlid.", "Atenció", INFORMATION_MESSAGE);
        } catch (MercatException mercatException) {
            JOptionPane.showMessageDialog(this, mercatException.getMessage(), "Atenció", INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAcceptaActionPerformed

